  <!-- Modal de notificaciones para móvil -->
  <div v-if="showMobileNotifications" class="fixed inset-0 z-50 md:hidden">
    <!-- Overlay -->
    <div class="absolute inset-0 bg-black bg-opacity-50" @click="closeMobileNotifications"></div>
    
    <!-- Modal content -->
